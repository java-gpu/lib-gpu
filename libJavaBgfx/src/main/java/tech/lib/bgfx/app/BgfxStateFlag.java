package tech.lib.bgfx.app;

import lombok.Getter;

public enum BgfxStateFlag {
    // No state
    NONE(0L),

    // Write masks
    WRITE_R(0x0000000000000001L),
    WRITE_G(0x0000000000000002L),
    WRITE_B(0x0000000000000004L),
    WRITE_RGB(WRITE_R.value | WRITE_G.value | WRITE_B.value),
    WRITE_A(0x0000000000000008L),
    WRITE_Z(0x0000000000000010L),
    WRITE_MASK(WRITE_R.value | WRITE_G.value | WRITE_B.value | WRITE_A.value | WRITE_Z.value),

    // Depth test functions
    DEPTH_TEST_LESS(0x0000000000000010L),
    DEPTH_TEST_LEQUAL(0x0000000000000020L),
    DEPTH_TEST_EQUAL(0x0000000000000030L),
    DEPTH_TEST_GEQUAL(0x0000000000000040L),
    DEPTH_TEST_GREATER(0x0000000000000050L),
    DEPTH_TEST_NOTEQUAL(0x0000000000000060L),
    DEPTH_TEST_NEVER(0x0000000000000070L),
    DEPTH_TEST_ALWAYS(0x0000000000000080L),
    DEPTH_TEST_MASK(0x00000000000000F0L),

    // Blending factors
    BLEND_ZERO(0x0000000000001000L),
    BLEND_ONE(0x0000000000002000L),
    BLEND_SRC_COLOR(0x0000000000003000L),
    BLEND_INV_SRC_COLOR(0x0000000000004000L),
    BLEND_SRC_ALPHA(0x0000000000005000L),
    BLEND_INV_SRC_ALPHA(0x0000000000006000L),
    BLEND_DST_ALPHA(0x0000000000007000L),
    BLEND_INV_DST_ALPHA(0x0000000000008000L),
    BLEND_DST_COLOR(0x0000000000009000L),
    BLEND_INV_DST_COLOR(0x000000000000A000L),
    BLEND_SRC_ALPHA_SAT(0x000000000000B000L),
    BLEND_FACTOR(0x000000000000C000L),
    BLEND_INV_FACTOR(0x000000000000D000L),
    BLEND_MASK(0x00000000000FF000L),

    // Blend equations
    BLEND_EQUATION_ADD(0x0000000000100000L),
    BLEND_EQUATION_SUB(0x0000000000200000L),
    BLEND_EQUATION_REVSUB(0x0000000000300000L),
    BLEND_EQUATION_MIN(0x0000000000400000L),
    BLEND_EQUATION_MAX(0x0000000000500000L),
    BLEND_EQUATION_MASK(0x0000000000F00000L),

    // Blend options
    BLEND_INDEPENDENT(0x0000000001000000L),
    BLEND_ALPHA_TO_COVERAGE(0x0000000002000000L),

    // Culling
    CULL_CW(0x0000000004000000L),
    CULL_CCW(0x0000000008000000L),
    CULL_MASK(0x000000000C000000L),

    // Front face
    FRONT_CCW(0x0000000010000000L),

    // Primitive types
    PT_TRISTRIP(0x0000000100000000L),
    PT_LINES(0x0000000200000000L),
    PT_LINESTRIP(0x0000000300000000L),
    PT_POINTS(0x0000000400000000L),
    PT_MASK(0x0000000F00000000L),

    // Miscellaneous
    MSAA(0x0000001000000000L),
    LINEAA(0x0000002000000000L),
    CONSERVATIVE_RASTER(0x0000004000000000L),

    // Default state
    DEFAULT(WRITE_RGB.value | WRITE_A.value | WRITE_Z.value | DEPTH_TEST_LESS.value | CULL_CW.value | MSAA.value);

    @Getter
    private final long value;

    BgfxStateFlag(long value) {
        this.value = value;
    }
}
